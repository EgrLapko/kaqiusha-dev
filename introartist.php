		<p class="text">
		&nbsp;&nbsp;&nbsp;&nbsp;苏联时期乌克兰的艺术大多注重于历史革命与情节，战时苏联人民英雄的战斗题材、肖像画、风景画均占据主要地位。我们的展览会为中国人民呈现苏联历史发展的最重要阶段，以及乌克兰和俄罗斯在这一时期的艺术代表性题材.
		</p>
		<p class="text">
		&nbsp;&nbsp;&nbsp;&nbsp;)确切的说，是在1917年的十月革命之后，苏联艺术界诞生了这样的名词“社会主义写实”(社会主义，现实主义二词的合成词）--20世纪苏联艺术创作的重要方向之一。这种艺术方向的美学理念始源于俄罗斯19世纪写实绘画的根基。工业化国家，在生产系统中向农业化结构状态转型，国家及人民物质,、精神极度紧张，而这样的艺术形式成为当时鼓舞人心的重要力量。         
		</p>
		<p class="text">
&nbsp;&nbsp;&nbsp;&nbsp; “社会主义写实”成为当时艺术界的普遍现象。它反映了生活中的变化和进步，记录了一个新的国家，新的社会秩序的形成。艺术大师的眼睛，捕捉到时代的变迁，和迅速更迭转变的意识形态。这些作品的价值就在于它们像一面镜子，映射出人们生活的时代。著名的社会主义写实代表作真正开创了这个先河，这些完成的作品，让我们感觉如同场景照片一样，充满存在感和现实感。在这个艺术形式中，存在指定的两个向量标准:一个是“官方的”要求具备史诗般的强大气魄，和在视图中夸张放大描叙事件的意义；第二个是“原始的”遵循固有的学术特点，和回归古典学院画派的意愿。创作的基本类型就是人们的日常生活，风景，静物。这个时期，在许多艺术家手下诞生了辉煌的原创作品，并使“原创”成为画家最普遍的价值观。        

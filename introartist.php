		          兰艺术文化的发展道路。革命之后乌克兰与其他苏维埃国家共同建立苏维埃政权联邦，国家经济实力增强，苏联社会
		           的国民经济与精神生活各方面得到了集中化发展，苏联兄弟民族之间艺术文化的交汇互映，对乌克兰肖像艺术的 发展产生了重要影响，促使它得到进一步的高涨和提升。            
